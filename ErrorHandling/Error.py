# Error handling 

# try except finally
# 
# 
# Try -- Try to do this (task) -- It will have the code that we actually want to execute.
try:
    print("hello")
except:
    # It will get executed if the try bloack throws an error
    print("Not able to print")
else:
    # this is like a confirmation that everything worked fine for try.
    print("We are good to go.")

finally:
    # Finally will be executed irrespective of try or except. 
    print("I will always be get executed.")

# WE use finally keyword (block) to close all the connections with file / Database / or other servers.



